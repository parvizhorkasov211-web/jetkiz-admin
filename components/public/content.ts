export function getLegalDocument(locale: Locale, slug: Exclude<LegalSlug, 'contacts'>) {
  return {
    meta: documents[locale].find((item) => item.slug === slug)!,
    sections: locale === 'kk' ? kkLegal[slug] : ruLegal[slug],
  };
}
