export function getDocuments(locale: Locale) {
  return documents[locale];
}

export function documentPath(locale: Locale, slug: LegalSlug) {
  if (slug === 'contacts') {
    return locale === 'kk' ? '/kk/contacts' : '/contacts';
  }

  return locale === 'kk' ? `/kk/${slug}` : `/${slug}`;
}

export function oppositeLocalePath(locale: Locale, slug?: LegalSlug) {
  if (!slug) {
    return locale === 'kk' ? '/' : '/kk';
  }

  return documentPath(locale === 'kk' ? 'ru' : 'kk', slug);
}

